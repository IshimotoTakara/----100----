# 02.「パタトクカシーー」
# 「パタトクカシーー」という文字列の1,3,5,7文字目を取り出して連結した文字列を得よ．
###############################################################
text0 = 'パトカー'
text1 = 'タクシー'
ans = ''

for i in range(len(text0)):
    ans += text0[i]
    ans += text1[i]

print(text0 + ' + ' + text1 + ' = ' + ans)
